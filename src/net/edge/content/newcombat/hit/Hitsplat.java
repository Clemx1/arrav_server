package net.edge.content.newcombat.hit;

/**
 * An enum of hitsplat types.
 *
 * @author Michael | Chex
 */
public enum Hitsplat {

    /** No hitsplat. */
    NONE,

    /** The regular hitsplat (red). */
    NORMAL_HIT,

    /** The block hitsplat (blue. */
    BLOCK,

    /** The poison hitsplat (green). */
    POISON,

    /** The disease hitsplat (yellow). */
    DISEASE,

    /** The venom hitsplat (teal). */
    VENOM

}
